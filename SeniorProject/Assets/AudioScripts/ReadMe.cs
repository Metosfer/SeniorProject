using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class ReadMe : ScriptableObject
{

    // Audio Wave Editor for Unity
    // Version: 1.0

//     Installation:

// Put AudioWaveEditor.cs in your project's Editor folder (create it if it doesn't exist: Assets/Editor/)
// The SavWav class is now defined in AudioWaveEditor, no separate file is needed

// Access:

// Open the window from the Window > Audio Wave Editor menu in Unity

// Features:

// Audio Clip selection: You can assign an AudioClip in the Inspector
// Waveform visualization: Sound waves are visually displayed
// Trim control: Set start/end points with sliders or by clicking on the waveform
// Mouse control:

// Left click: Set start point
// Shift + Left click: Set end point
// Drag: Adjust selection area

// Play: Play only the selected area
// Export: Export the selected area as a WAV file
// Visual settings: You can change the waveform color and height

// Controls:

// Play Selected: Play the selected area
// Pause/Resume: Stop playing pause/resume
// Stop: Stops playback
// Export Selected Region: Saves the selected region as WAV

}
